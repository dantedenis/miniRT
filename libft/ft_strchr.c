char	*ft_strchr(const char *s, int c)
{
	while (*s != '\0')
	{
		if (*s == (char) c)
			return ((char *) s);
		++s;
	}
	if (*s != (char) c)
		return ((void *) 0);
	else
		return ((char *) s);
}
